"use strict"

/**
 * Функция умножает a и b
 *@param {number} a
 *@param {number} b
 *@returns {number} a * b
 */
function multiplication(a, b) {
    return a * b;
}

/**
 * Функция делит a на b
 *@param {number} a
 *@param {number} b
 *@returns {number} a / b
 */
function division(a, b) {
    return a / b;
}

/**
 * Функция складывает a и b
 *@param {number} a
 *@param {number} b
 *@returns {number} a + b
 */
function addition(a, b) {
    return a + b;
}

/**
 * Функция вычитает из a  b
 *@param {number} a
 *@param {number} b
 *@returns {number} a - b
 */
function subtraction(a, b) {
    return a - b;
}